    "SET profiling = 1;\n",
    "SELECT * FROM orders WHERE user_id = 12345 ORDER BY created_at DESC;\n",
    "SHOW PROFILE FOR QUERY 1;\n",
    "\n",
    "-- 4. sys 스키마 활용\n",
    "SELECT * FROM sys.schema_index_stats WHERE table_name='orders';\n",
    "```\n",
