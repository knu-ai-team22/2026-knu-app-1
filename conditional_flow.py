    "## 3. 우선순위가 높은 최적화 단계\n",
    "\n",
    "> **우선순위**: 1) 인덱스 → 2) 쿼리 리팩토링(불필요한 컬럼/정렬) → 3) 파티셔닝/스토리지 엔진 최적화 → 4) 캐싱 → 5) 비동기/배치 처리\n",
    "\n",
    "### 단계 1: 인덱스 최적화\n",
    "\n",
    "| 상황 | 인덱스 제안 | 예상 효과 |\n",
    "|------|-------------|-----------|\n",
    "| `WHERE user_id = ?` | `CREATE INDEX idx_user_id ON orders(user_id);` | 테이블 스캔 → 인덱스 스캔 (10만 → 10 KB) |\n",
    "| `WHERE user_id = ? ORDER BY created_at DESC` | `CREATE INDEX idx_user_created ON orders(user_id, created_at DESC);` | 정렬 비용 절감 (인덱스 자체 정렬) |\n",
    "| `WHERE status = ? AND created_at BETWEEN ? AND ?` | `CREATE INDEX idx_status_date ON orders(status, created_at);` | 조합 인덱스 사용 |\n",
    "\n",
    "> **실제 예시**  \n",
    "> - 인덱스 없을 때: `SELECT * FROM orders WHERE user_id=1` → 5 초  \n",
    "> - 인덱스 추가 후: 동일 쿼리 → 0.4 초 (12.5배 빠름)\n",
    "\n",
    "### 단계 2: 불필요한 컬럼 및 정렬 제거\n",
    "\n",
    "1. **SELECT * → 필요한 컬럼만 지정**  \n",
    "   ```sql\n",
    "   SELECT id, status, created_at FROM orders WHERE user_id = 12345;\n",
    "   ```\n",
    "   *컬럼 수 5KB → 1KB, 네트워크 4배 감소*\n",
    "\n",
    "2. **ORDER BY/ GROUP BY 최소화**  \n",
    "   - 정렬이 꼭 필요한 경우 인덱스에 포함  \n",
    "   - 아니면 `LIMIT` 없이 정렬은 피함\n",
    "\n",
    "3. **서브쿼리 → JOIN 변환**  \n",
    "   ```sql\n",
    "   SELECT o.id, o.status, u.name\n",
    "   FROM orders o\n",
    "   JOIN users u ON o.user_id = u.id\n",
    "   WHERE o.user_id = 12345;\n",
    "   ```\n",
    "\n",
    "### 단계 3: 파티셔닝 및 스토리지 엔진\n",
    "\n",
    "| 방법 | 장점 | 적용 예시 |\n",
    "|------|------|-----------|\n",
    "| **테이블 파티셔닝 (Range, Hash)** | 특정 파티션만 스캔 → I/O 감소 | `PARTITION BY RANGE (YEAR(created_at))` |\n",
    "| **InnoDB → MyISAM** | 읽기 전용 시 MyISAM이 빠를 수 있음 | **권장**: 대부분 InnoDB 사용, MyISAM은 읽기 전용 데이터에만 |\n",
    "| **압축** | 디스크 I/O 감소 | `ROW_FORMAT=COMPRESSED` |\n",
    "\n",
    "> **예시**  \n",
    "> `orders` 테이블이 10만 행이라도 월별 파티셔닝으로 `2024-04` 파티션만 스캔 → 2배 속도 향상\n",
    "\n",
    "### 단계 4: 캐싱\n",
    "\n",
    "| 캐시 종류 | 구현 방법 | 예상 효과 |\n",
    "|-----------|----------|-----------|\n",
    "| **Query Cache** (MySQL 5.7 이하) | `query_cache_type=ON` | 동일 쿼리 재실행 시 0.1 초 |\n",
    "| **Redis / Memcached** | `SELECT * FROM orders WHERE user_id=?` 결과를 캐시 | 80 %~90 % 요청 캐시율 → 0.05 초 |\n",
    "| **Application Layer Cache** | `@Cacheable` (Spring) | 재사용 가능한 비즈니스 데이터 캐시 |\n",
    "\n",
    "> **주의**: MySQL 8.0 이상에서는 Query Cache 비활성화, 대신 애플리케이션 레벨 캐시 권장.\n",
    "\n",
    "### 단계 5: 비동기 / 배치 처리\n",
    "\n",
    "1. **비동기 DB 호출**  \n",
    "   - `CompletableFuture` / `async` API 사용 → UI 블로킹 최소화\n",
    "2. **배치 업데이트**  \n",
    "   - `INSERT … ON DUPLICATE KEY UPDATE` → 10만 행을 한 번에 삽입\n",
    "3. **ETL/데이터 웨어하우스**  \n",
    "   - 실시간 조회가 필요 없으면 별도 OLAP DB에 복제\n",
