    "## 2. 프로파일링 및 성능 측정 방법\n",
    "\n",
    "| 단계 | 도구 / 명령 | 목표 |\n",
    "|------|-------------|------|\n",
    "| 1 | **`EXPLAIN` / `EXPLAIN ANALYZE`** | 쿼리 실행 계획 확인 → Full Table Scan 여부 |\n",
    "| 2 | **`SHOW WARNINGS`** | 실행 계획에 경고가 있는지 확인 |\n",
    "| 3 | **`mysqldumpslow`** | 가장 오래 걸리는 쿼리 10개 추출 |\n",
    "| 4 | **`pt-query-digest`** (Percona Toolkit) | 로그를 분석 → 평균 실행 시간, I/O, CPU |\n",
    "| 5 | **`performance_schema`** | `events_statements_current`, `events_statements_history` 조회 |\n",
    "| 6 | **`mysqlslap`** | 부하 테스트 → 동시 연결 수와 응답 시간 |\n",
    "| 7 | **`sys schema`** (Percona) | `sys.schema_table_stats`, `sys.schema_index_stats` 등으로 인덱스 사용 현황 확인 |\n",
    "| 8 | **`InnoDB Buffer Pool Size`** | 메모리 캐시 비율(%) |\n",
    "| 9 | **`SHOW PROCESSLIST`** | 대기 중인 쿼리와 잠금 현황 |\n",
    "\n",
    "**측정 절차 예시**\n",
    "\n",
